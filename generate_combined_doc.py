BASE = r"d:\edr_final_project\9_version\edr-platform"
CH1  = os.path.join(BASE, "EDR-Documentaion-Chapter-1-V1.docx")
OUT  = os.path.join(BASE, "EDR_Documentation_Combined.docx")
BLACK = RGBColor(0, 0, 0)
FONT  = "Traditional Arabic"

shutil.copy(CH1, OUT)
doc = Document(OUT)

def _rp(para, align='right'):
    pPr = para._p.get_or_add_pPr()
    bd = pPr.find(qn('w:bidi'))
    if bd is None: bd = OxmlElement('w:bidi'); pPr.insert(0, bd)
    bd.set(qn('w:val'), '1')
    jc = pPr.find(qn('w:jc'))
    if jc is None: jc = OxmlElement('w:jc'); pPr.append(jc)
    jc.set(qn('w:val'), align)

def _rr(run):
    rPr = run._r.get_or_add_rPr()
    r = rPr.find(qn('w:rtl'))
    if r is None: r = OxmlElement('w:rtl'); rPr.append(r)
    r.set(qn('w:val'), '1')

def _mk(para, text, sz, bold, italic=False):
    r = para.add_run(text); _rr(r)
    r.font.name = FONT; r.font.size = Pt(sz)
    r.font.bold = bold; r.font.italic = italic
    r.font.color.rgb = BLACK

def P(text, sz=12, bold=False, align='right', sb=0, sa=5, italic=False):
    p = doc.add_paragraph(); _rp(p, align)
    p.paragraph_format.space_before = Pt(sb)
    p.paragraph_format.space_after  = Pt(sa)
    if text: _mk(p, text, sz, bold, italic)
    return p

def H1(t):
    p = doc.add_paragraph(); _rp(p, 'center')
    p.paragraph_format.space_before = Pt(20); p.paragraph_format.space_after = Pt(12)
    _mk(p, t, 18, True); return p

def H2(t):
    p = doc.add_paragraph(); _rp(p, 'right')
    p.paragraph_format.space_before = Pt(14); p.paragraph_format.space_after = Pt(6)
    _mk(p, t, 15, True); return p

def H3(t):
    p = doc.add_paragraph(); _rp(p, 'right')
    p.paragraph_format.space_before = Pt(10); p.paragraph_format.space_after = Pt(4)
    _mk(p, t, 13, True); return p

def H4(t):
    p = doc.add_paragraph(); _rp(p, 'right')
    p.paragraph_format.space_before = Pt(6); p.paragraph_format.space_after = Pt(2)
    _mk(p, t, 12, True); return p

def BL(text):
    p = doc.add_paragraph(); _rp(p, 'right')
    p.paragraph_format.space_before = Pt(0); p.paragraph_format.space_after = Pt(3)
    _mk(p, "• " + text, 11, False); return p

def page_break():
    p = doc.add_paragraph(); r = p.add_run()
    br = OxmlElement('w:br'); br.set(qn('w:type'), 'page'); r._r.append(br)

def add_table(headers, rows, caption=None):
    if caption: P(caption, bold=True, sb=8, sa=3, sz=11)
    tbl = doc.add_table(rows=1+len(rows), cols=len(headers))
    tbl.style = 'Table Grid'
    tPr = tbl._tbl.find(qn('w:tblPr'))
    if tPr is None: tPr = OxmlElement('w:tblPr'); tbl._tbl.insert(0, tPr)
    tPr.append(OxmlElement('w:bidiVisual'))
    tW = OxmlElement('w:tblW'); tW.set(qn('w:w'), '9200'); tW.set(qn('w:type'), 'dxa'); tPr.append(tW)
    for i, h in enumerate(headers):
        c = tbl.rows[0].cells[i]; c.text = ''; p = c.paragraphs[0]; _rp(p, 'center'); _mk(p, h, 11, True)
        tcPr = c._tc.get_or_add_tcPr(); shd = OxmlElement('w:shd')
        shd.set(qn('w:val'), 'clear'); shd.set(qn('w:color'), 'auto'); shd.set(qn('w:fill'), 'BDD7EE'); tcPr.append(shd)
    for ri, rd in enumerate(rows):
        for ci, val in enumerate(rd):
            c = tbl.rows[ri+1].cells[ci]; c.text = ''; p = c.paragraphs[0]; _rp(p, 'right'); _mk(p, str(val), 10, False)
    P('', sb=0, sa=4); return tbl

def blacken_all():
    for p in doc.paragraphs:
        for r in p.runs: r.font.color.rgb = BLACK
    for t in doc.tables:
        for row in t.rows:
            for c in row.cells:
                for p in c.paragraphs:
                    for r in p.runs: r.font.color.rgb = BLACK

# ── Blacken Chapter 1 & add page break ───────────────────────────────────────
blacken_all()
page_break()

